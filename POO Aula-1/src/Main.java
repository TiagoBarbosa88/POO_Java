public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");

        // byte -128 a 127
        byte variavelByte = 127;
        System.out.println(variavelByte);

        //short -32768 a 32767
        short variavelShort = 32760;
        System.out.println(variavelShort);

        // char 0 a 65535
        char variavelChar = 50000;
        System.out.println(variavelChar);

        // int -2b  a 2b
        int variavelInt = 2000000;
        System.out.println(variavelInt);

        // long -9t a 9t
        long variavelLong = 70000000;
        System.out.println(variavelLong);

    }
}